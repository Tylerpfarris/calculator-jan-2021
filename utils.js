

export function getAdditionSum(num1, num2) {
    return num1 + num2;
};

export function getSubtractionSum(num1, num2) {
    return num1 - num2;
};

export function getMultiplicationSum(num1, num2) {
    return num1 * num2;
};

export function getDivisionSum(num1, num2) {
    return num1 / num2;
};
